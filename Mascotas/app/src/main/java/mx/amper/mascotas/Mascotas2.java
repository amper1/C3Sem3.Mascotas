public class Mascotas2 {
    private int foto2;
    private String ranking2;


    public Mascotas2(int foto2, String ranking2){
        this.foto2 = foto2;
        this.ranking2 = ranking2;
    }

    public int getFoto() {
        return foto2;
    }

    public void setFoto(int foto) {
        this.foto2 = foto;
    }

    public String getRanking() {
        return ranking2;
    }

    public void setRanking(String ranking) {
        this.ranking2 = ranking;
    }
}
